package BasicJavaProgram;

public class predefinedMath {
	public static void main(String[] args) {
		System.out.println(Math.addExact(100, 200));
		System.out.println(Math.addExact(12345l, 67899l));
		System.out.println(Math.max(100.23, 200.23));
		System.out.println(Math.max(100.2f, 200.2f));
		System.out.println(Math.max(12345l, 7890l));
		System.out.println(Math.min(10, 20));
		System.out.println(Math.min(3456.23f, 2345f));
		System.out.println(Math.abs(-23.56));
		System.out.println(Math.random());
		System.out.println(Math.PI);
		
	}

}
